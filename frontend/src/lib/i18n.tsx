const LOCALES: Record<Locale, Messages> = {
  en: { ...en, guideExamplePrompts: [...en.guideExamplePrompts] },
  "zh-CN": { ...zhCN },
};

// ---- Context ----
interface I18nCtxValue {
  locale: Locale;
  t: Messages;
  setLocale: (l: Locale) => void;
}

const I18nCtx = createContext<I18nCtxValue>({
  locale: "en",
  t: en,
  setLocale: () => {},
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(getInitialLocale);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch {}
  }, []);

  // Sync across tabs
  useEffect(() => {
    const handler = (e: StorageEvent) => {
      if (e.key === STORAGE_KEY && (e.newValue === "en" || e.newValue === "zh-CN")) {
        setLocaleState(e.newValue);
      }
    };
    window.addEventListener("storage", handler);
    return () => window.removeEventListener("storage", handler);
  }, []);

  const t = LOCALES[locale];

  return (
    <I18nCtx.Provider value={{ locale, t, setLocale }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n() { return useContext(I18nCtx); }
